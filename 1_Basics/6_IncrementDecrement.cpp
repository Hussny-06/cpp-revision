#include<iostream>
using namespace std;

int main(){
    int a;
    cin>>a;
    cout<<"value of a is :"<<a<<endl;
    cout<<"effect of a++ is :"<<a++<<endl;
    cout<<"value of a now is :"<<a<<endl;
    cout<<"effect of ++a is :"<<++a<<endl;
    cout<<"value of a is :"<<a<<endl;

    cout<<endl<<endl;
    int b;
    cin>>b;
    cout<<"value of b is :"<<b<<endl;
    cout<<"effect of b-- is :"<<b--<<endl;
    cout<<"value of b now is :"<<b<<endl;
    cout<<"effect of --b is :"<<--b<<endl;
    cout<<"value of b is :"<<b<<endl;
}